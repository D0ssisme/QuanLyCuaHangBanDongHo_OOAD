-- 11. SendOTP
CREATE PROCEDURE SendOTP
    @OTP NVARCHAR(255),
    @EMAIL NVARCHAR(255)
AS
BEGIN
    UPDATE TAIKHOAN 
    SET OTP = @OTP 
    FROM TAIKHOAN TK
    JOIN NHANVIEN NV ON TK.MNV = NV.MNV 
    WHERE NV.EMAIL = @EMAIL
END
GO

-- 12. CheckOTP
CREATE PROCEDURE CheckOTP
    @EMAIL NVARCHAR(255),
    @OTP NVARCHAR(255)
AS
BEGIN
    SELECT TK.* FROM TAIKHOAN TK 
    JOIN NHANVIEN NV ON TK.MNV = NV.MNV 
    WHERE NV.EMAIL = @EMAIL AND TK.OTP = @OTP
END
GO
